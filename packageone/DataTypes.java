/*Show a program on which shows java execution is a line by line executions*/
package packageone;

public class DataTypes {

	public static void main(String[] args) 
	{
		byte a1; //declaration
		a1=100; //initialization
		System.out.println(a1);
		short a2=3300; // declaration & initialization in same line
		System.out.println(a2);
		int a3=100000000;
		System.out.println(a3);
		float a4=23.99f;
		System.out.println(a4);
		double a5=45.876565;
		System.out.println(a5);
		char a6='R';
		System.out.println(a6);
		boolean a7=true;
		System.out.println(a7);
		String a8="Hey, how are you?";
		System.out.println(a8);	
		
				

	}

}
